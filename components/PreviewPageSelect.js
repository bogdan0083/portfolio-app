function classNames(...classes) {
  return classes.filter(Boolean).join(' ')
}

export default function PreviewPageSelect({pages, onSelected}) {
  const [selected, setSelected] = useState(pages[0])

  const onSelectChange = item => {
      setSelected(item);
      onSelected(item);
  }

  return (
    <Listbox value={selected} onChange={onSelectChange}>
      <div className="relative sm:ml-2 md:ml-6 w-44">
        <Listbox.Button className="relative w-full bg-white border border-gray-300 shadow-sm pl-3 pr-10 py-2 text-left focus:outline-none focus:ring-1 focus:ring-indigo-500 focus:border-indigo-500 sm:text-sm" style={{minHeight: 42}}>
          <span className="flex items-center font-semibold text-space-default">
            {selected.title}
          </span>
          <span className="ml-3 absolute inset-y-0 right-0 flex items-center pr-2 pointer-events-none">
            <SelectorIcon className="h-5 w-5 text-space-default" aria-hidden="true" />
          </span>
        </Listbox.Button>

        <Transition as={Fragment} leave="transition ease-in duration-100" leaveFrom="opacity-100" leaveTo="opacity-0">
          <Listbox.Options className="absolute z-10 mt-1 w-full bg-white shadow-lg max-h-56 py-1 text-base ring-1 ring-black ring-opacity-5 overflow-auto focus:outline-none sm:text-sm">
            {pages.map((page) => (
              <Listbox.Option
                key={page.slug}
                className={({ active }) =>
                  classNames(
                    active ? 'text-white bg-indigo-600' : 'text-gray-900',
                    'cursor-default select-none relative py-2 pl-3 pr-2'
                  )
                }
                value={page}
              >
                {({ selected, active }) => (
                  <>
                    <div className="flex items-center">
                      <span className={classNames(selected ? 'font-semibold' : 'font-normal', 'block truncate')}>
                        {page.title}
                      </span>
                    </div>

                    {selected ? (
                      <span
                        className={classNames(
                          active ? 'text-white' : 'text-indigo-600',
                          'absolute inset-y-0 right-0 flex items-center pr-1'
                        )}
                      >
                      </span>
                    ) : null}
                  </>
                )}
              </Listbox.Option>
            ))}
          </Listbox.Options>
        </Transition>
      </div>
    </Listbox>
  )
}